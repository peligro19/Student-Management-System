    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
            stmt = conn.createStatement();
            int stdID = Integer.parseInt(jid.getText());
            String sql = "SELECT * FROM STUDENT WHERE id = '"+stdID+"' ";
            rs = stmt.executeQuery(sql);
            
            if(rs.next()){
                
                jname.setText(rs.getString("stdName"));
                jfathername.setText(rs.getString("stdFatherName"));
                jcity.setText(rs.getString("stdCity"));
                jblood.setText(rs.getString("stdBlood"));
                jphone.setText(rs.getString("stdPhone"));
                jclass.setText(String.format("%s",rs.getInt("class")));
               
            }
            else{
                JOptionPane.showMessageDialog(null,"Record Not Found");
            }
                
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
            
    }//GEN-LAST:event_jButton3ActionPerformed

