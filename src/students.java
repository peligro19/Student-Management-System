    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Adding Data to the DataBase
        try{

            stmt = conn.createStatement();
            int stdId = Integer.parseInt(jid.getText());
            String stdName = jname.getText();
            String stdFather = jfathername.getText();
            String stdBlood = jblood.getText();
            String stdPhone = jphone.getText();
            String stdCity = jcity.getText();
            String stdClass = jclass.getText();

            String sql = "UPDATE STUDENT SET stdName='"+stdName+"' , stdFatherName='"+stdFather+"' , stdBlood='"+stdBlood+"' , stdPhone='"+stdPhone+"' , stdCity='"+stdCity+"' , class='"+stdClass+"' WHERE id ='"+stdId+"' ";
            stmt.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Data Successfully Updated");
            
            //for clearing previous data
            jid.setText("");
            jname.setText("");
            jfathername.setText("");
            jblood.setText("");
            jphone.setText("");
            jcity.setText("");
            jclass.setText("");

        }catch(HeadlessException | NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

